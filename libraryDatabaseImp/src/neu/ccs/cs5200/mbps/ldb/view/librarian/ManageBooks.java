    private void delete_deleteBookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_deleteBookButtonActionPerformed
        // search for the books that match the user parameters
                // search on the selected field
        String attribute = (String) this.delete_bookAttributeComboBox.getSelectedItem();
        String value = this.delete_bookAttributeTextField.getText();
        
        // load the book results table into memory to manage it
        DefaultTableModel m = (DefaultTableModel) delete_deleteBooksTable.getModel();

        // wipe out the results table
        while (m.getRowCount() > 0) {
            m.removeRow(0);
        }

        // search the database for books matching the select fields
        List<Book> books = (LDBConnector.getInstance()).searchBook(attribute, value);
        
        // add the books to the results table
        for (Book b : books) {
            m.addRow(new Object[] { false,
                                    b.getId(),
                                    b.getIsbn(),
                                    b.getTitle(),
                                    b.getAuthor(),
                                    b.getPublisher(),
                                    b.getGenre()
            });
        }

        // navigate back to the manage books screen
        CardLayout cl = (CardLayout) deleteBookPanel.getLayout();
        cl.show(deleteBookPanel, ViewConstants.Librarian.ManageBooks.DeleteBook.DELETE_BOOK);
    }//GEN-LAST:event_delete_deleteBookButtonActionPerformed

    private void searchEditBookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchEditBookButtonActionPerformed
        // edit the current book's fields
        
        // search on the selected field
        String attribute = (String) this.bookAttributeComboBox.getSelectedItem();
        String value = this.bookAttributeTextField.getText();
        
        List<Book> books = (LDBConnector.getInstance()).searchBook(attribute, value);
        if (books.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                                          "No results for selected attribute",
                                          "No Results",
                                          JOptionPane.WARNING_MESSAGE
            );
        } else {
            Book book = books.get(0);
            
            this.bookIdTextLabel.setText(Integer.toString(book.getId()));
            this.edit_isbnTextField.setText(book.getIsbn());
            this.edit_titleTextField.setText(book.getTitle());
            this.edit_authorTextField.setText(book.getAuthor());
            this.edit_publisherTextField.setText(book.getPublisher());
            this.edit_genreComboBox.setSelectedIndex(Genre.indexOf(book.getGenre()));
            
            // navigate to the edit screen
            CardLayout cl = (CardLayout) editBookPanel.getLayout();
            cl.show(editBookPanel, ViewConstants.Librarian.ManageBooks.EditBook.EDIT_BOOK);
        }
    }//GEN-LAST:event_searchEditBookButtonActionPerformed

    private void deleteBooksButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBooksButtonActionPerformed
        // delete all of the selected items
        DefaultTableModel m = (DefaultTableModel) delete_deleteBooksTable.getModel();
        for (int i=0; i<m.getRowCount(); i++) {
            if ((Boolean) m.getValueAt(i, 0)) {
                // delete the book from the database by id
                int id = (Integer) m.getValueAt(i, 1);
                (LDBConnector.getInstance()).deleteBook(id);
                
                // remove the row from the current table
                m.removeRow(i);
                i--; // adjust row index so it stays accurate
            }
        }

        JOptionPane.showMessageDialog(this,
                                      "Book successfully deleted!",
                                      "Book Deleted",
                                      JOptionPane.INFORMATION_MESSAGE
        );

        // switch the view to the book search results
        CardLayout cl = (CardLayout) getLayout();
        cl.show(this, ViewConstants.Librarian.MANAGE_BOOKS);
    }//GEN-LAST:event_deleteBooksButtonActionPerformed

