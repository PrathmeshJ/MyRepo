    /**
     * load the manage books page
     */
    public void loadManageBooks() {
        // set the dropdown box items
        DefaultComboBoxModel cbm1 = (DefaultComboBoxModel) add_genreComboBox.getModel();
        for (int i=0; i<Genre.values().length; i++) {
            cbm1.addElement(Genre.values()[i].getText());
        }
        DefaultComboBoxModel cbm2 = (DefaultComboBoxModel) edit_genreComboBox.getModel();
        for (int i=0; i<Genre.values().length; i++) {
            cbm2.addElement(Genre.values()[i].getText());
        }
        
        registerManageBooks();
    }

    
