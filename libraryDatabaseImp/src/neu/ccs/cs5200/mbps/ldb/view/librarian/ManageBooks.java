    private void addBookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBookButtonActionPerformed
        // add a book to the database
        
        // get the data from the fields
        String title = this.add_titleTextField.getText();
        String isbn = this.add_isbnTextField.getText();
        String author = this.add_authorTextField.getText();
        String publisher = this.add_publisherTextField.getText();
        int genreIndex = this.add_genreComboBox.getSelectedIndex();
        
        // only advance if all fields are filled out
        if (title.isEmpty() || isbn.isEmpty() || author.isEmpty() || publisher.isEmpty() || genreIndex == -1) {
            JOptionPane.showMessageDialog(this,
                                          "Please fill in all fields",
                                          "Missing Information",
                                          JOptionPane.WARNING_MESSAGE
            );
        }
        
        // fill in the book fields
        Book book = new Book();
        book.setTitle(title);
        book.setIsbn(isbn);
        book.setAuthor(author);
        book.setPublisher(publisher);
        book.setGenre(Genre.values()[genreIndex].getText());
        
        // add the book to the database
        (LDBConnector.getInstance()).addBook(book);
        
        JOptionPane.showMessageDialog(this,
                                      "Book successfully added to the library!",
                                      "Book Added",
                                      JOptionPane.INFORMATION_MESSAGE
        );
        
        // navigate back to the manage books screen
        CardLayout cl = (CardLayout) getLayout();
        cl.show(this, ViewConstants.Librarian.MANAGE_BOOKS);
    }//GEN-LAST:event_addBookButtonActionPerformed

